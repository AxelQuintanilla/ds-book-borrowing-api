DROP TABLE IF EXISTS ARTISTS;
CREATE TABLE ARTISTS (
	ARTIST_ID BIGINT NOT NULL AUTO_INCREMENT,
	NAME VARCHAR(45) NOT NULL,
	PHONE VARCHAR(12) NOT NULL,
	AGE INTEGER NOT NULL,
	CREATION_DATE TIMESTAMP NOT NULL,
	PRIMARY KEY (ARTIST_ID)
);


DROP TABLE IF EXISTS TRACKS CASCADE;
CREATE TABLE TRACKS (
	TRACK_ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
	ISRC VARCHAR(12) NOT NULL,
	DURATION INTEGER NOT NULL,
	ARTIST_ID BIGINT NOT NULL,
    CREATION_DATE TIMESTAMP NOT NULL,
    PRIMARY KEY (TRACK_ID),
    CONSTRAINT FK_ARTIST_ID  FOREIGN KEY (ARTIST_ID) REFERENCES ARTISTS (ARTIST_ID)
);

DROP TABLE IF EXISTS ALBUMS;
CREATE TABLE ALBUMS (
	ALBUM_ID BIGINT NOT NULL AUTO_INCREMENT,
	AUID VARCHAR(12) NOT NULL,
	NAME VARCHAR(45) NOT NULL,
	CREATION_DATE TIMESTAMP NOT NULL,	
	PRIMARY KEY (ALBUM_ID)
);


DROP TABLE IF EXISTS DISCOGRAPHYS;
CREATE TABLE DISCOGRAPHYS (
	DISCOGRAPHY_ID BIGINT NOT NULL AUTO_INCREMENT,
	COMMERCIAL_NAME VARCHAR(45) NOT NULL,
	LEGAL_NAME VARCHAR(45) NOT NULL,
	YEAR INTEGER NOT NULL,
	CREATION_DATE TIMESTAMP NOT NULL,	
	PRIMARY KEY (DISCOGRAPHY_ID)
);

DROP TABLE IF EXISTS GENRES;
CREATE TABLE GENRES (
	GENRE_ID BIGINT NOT NULL AUTO_INCREMENT,
	GENRE_NAME VARCHAR(45) NOT NULL,
	CREATION_DATE TIMESTAMP NOT NULL,	
	PRIMARY KEY (GENRE_ID)
);